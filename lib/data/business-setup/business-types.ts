export interface SetupCostItem {
  name: string;
  /** טווח מחיר ₪ (חד-פעמי) */
  min: number;
  max: number;
  /** פריט חובה (true) או אופציונלי (false) */
  essential: boolean;
  note?: string;
}

export interface MonthlyCostItem {
  name: string;
  /** ₪ לחודש (קבוע; לא כולל שכ"ד/ארנונה/שכר) */
  amount: number;
  note?: string;
}

export interface BusinessType {
  slug: string;
  /** שם קצר: "סטודיו פילאטיס" */
  name: string;
  category: string;
  /** ביטויי חיפוש שהעמוד מכוון אליהם */
  keywords: string[];
  /** תיאור קצר לכותרת ולמטא */
  intro: string;
  /** שטח טיפוסי/מינימום/מקסימום במ"ר */
  typicalAreaSqm: number;
  minAreaSqm: number;
  maxAreaSqm: number;
  /** עלות שיפוץ/התאמה למקום חדש — ₪ למ"ר */
  renovationPerSqm: { min: number; max: number };
  /** רישוי, אגרות, אישורים — חד-פעמי ₪ */
  licensing: { min: number; max: number };
  /** אורך חיים ממוצע של הציוד והשיפוץ (שנים) — לחישוב פחת ורזרבת חידוש */
  equipmentUsefulLifeYears: number;
  /** פריטי הקמה (ציוד, ריהוט, מלאי פתיחה...) */
  setupItems: SetupCostItem[];
  /** הוצאות חודשיות קבועות (מלבד שכ"ד/ארנונה/שכר) */
  monthlyItems: MonthlyCostItem[];
  /** כוח אדם (מלבד הבעלים) */
  staff: { count: number; avgMonthlyGross: number; note?: string };
  /** הכנסה: מחיר ממוצע ליחידת מכירה + תווית */
  revenue: { avgTicket: number; unitLabel: string; note?: string };
  /** שיעור עלות משתנה מההכנסה (חומרים/סחורה) — לחישוב תרומה */
  variableCostRate: number;
  /** פסקאות מדריך */
  guide: string[];
  /** שאלות נפוצות */
  faq: { q: string; a: string }[];
}

